"""Examples of using the maths module."""

import math

# Round up
print("Ceil of 4.4 = ", math.ceil(4.4))
# Round Down
print("Floor of 4.4 = ", math.floor(4.4))
# Abosolut (No Sign)
print("Fabs of -4.4 = ", math.fabs(-4.4))
# Factorial 1*2*3*4
print("Factorial of 4 = ", math.factorial(4))
# Returns remainder
print("Modulas of 5/4", math.fmod(5, 4))
# Strips the numbers after decimal place
print("Truncate Float 4.2 = ", math.trunc(4.2))
# Number to the power of 2^2
print("2 to the power of 2 = = ", math.pow(2, 2))
# Square root of a number
print("The square root of 4 = ", math.sqrt(4))
# Special values
print("Eulars number = ", math.e)
print("Pi number = ", math.pi)
# Return e^x
print("Expential of 4 = ", math.exp(4))
# Return the natural logarithm e * e * e ~= 20 so log(20) tells
# you that e^3 ~= 20
print("log(20) = ", math.log(20))
# You can define the base and 10^3 = 1000
print("log(1000,10) = ", math.log(1000, 10))
# You can also use base 10 like this
print("log10(1000) = ", math.log10(1000))
# We have the following trig functions
# sin, cos, tan, asin, acos, atan, atan2, asinh, acosh,
# atanh, sinh, cosh, tanh
# They follow this format
print("sin(0) ", math.sin(0))
# Convert radians to degrees and vice versa
print("degrees(1.5708) = ", math.degrees(1.5708))
print("radians(90) = ", math.radians(90))
